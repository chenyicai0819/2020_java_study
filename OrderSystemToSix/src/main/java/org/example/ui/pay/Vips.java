package org.example.ui.pay;

public class Vips {
    private int vid;
    private String vname;
    private String vpassword;
    private String phone;
    private double vintegral;
    private String vgrade;

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }

    public String getVname() {
        return vname;
    }

    public void setVname(String vname) {
        this.vname = vname;
    }

    public String getVpassword() {
        return vpassword;
    }

    public void setVpassword(String vpassword) {
        this.vpassword = vpassword;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public double getVintegral() {
        return vintegral;
    }

    public void setVintegral(double vintegral) {
        this.vintegral = vintegral;
    }

    public String getVgrade() {
        return vgrade;
    }

    public void setVgrade(String vgrade) {
        this.vgrade = vgrade;
    }
}
